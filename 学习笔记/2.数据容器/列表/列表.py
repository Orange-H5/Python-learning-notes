
# 定义一个列表list
my_list1 = ['我爱你', 'hi', '好的']
print(my_list1)
print(type(my_list1))

my_list2 = ['我爱你', 666, True]
print(my_list2)
print(type(my_list2))


# 定义一个嵌套列表
my_list3 = [[1, 2, 3], [4, 5, 6]]
print(my_list3)
print(type(my_list3))

# 通过下标索引出对应位置的元素
my_list4 = ['Tom', 'Lily', 'Rose']
print(my_list4[0])
print(my_list4[1])
print(my_list4[2])

# 倒序取出
print(my_list4[-1])
print(my_list4[-2])
print(my_list4[-3])

# 取出嵌套列表的元素
my_list5 = [[1, 2, 3], [4, 5, 6]]
print(my_list5[1][1])
