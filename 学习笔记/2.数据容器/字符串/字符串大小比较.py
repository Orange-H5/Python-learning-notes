"""
字符串大小比较(按位比较，一一比对，一位大整体就大）
"""

# 比较abc和abd
print(f"abc大于abd结果：{'abc'>'abd'}")

# 比较a和ab
print(f"a大于ab结果：{'a'>'ab'}")

# 比较ab和c
print(f"ab大于c结果：{'ab'>'c'}")

# 比较a和A
print(f"a大于A结果：{'a'>'A'}")

# 比较key1和key2
print(f"key1大于key2结果：{'key1'>'key2'}")

# ord()将字符转为AscII码
print(ord('A'))

# chr()将AscII数值码转成字符
print(chr(65))

