
# 字符串的定义

name_1 = '我要学好python'
name_2 = "我要学好python"
name_3 = """我要学好python"""
print(name_1)
print(name_2)
print(name_3)

print("-------------")

# 字符串内包含单引号
name_4 = "'我爱你'"
print(name_4)

# 字符串内包含双引号
name_5 = '"我爱你"'
print(name_5)

print("-------------")

# 使用转义字符来解除引号的效用
name_6 = "\"我喜欢你\""
name_7 = '\'我真的喜欢你\''
print(name_6)
print(name_7)
