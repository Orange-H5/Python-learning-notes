
# 字符串转数值
str_int = int("666")
str_float = int("13")
print(type(str_int), str_int)
print(type(str_float), str_float)
print("-------------")

# 数值转字符串
int_str = str(6)
float_str = str(13.14)
print(type(int_str), int_str)
print(type(float_str), float_str)
print("------------")

# 整数转浮点数
int_float = float(666)
print(type(int_float), int_float)
print("------------")

# 浮点数转整数(直接舍去小数部分，不考虑四舍五入）
float1_int = int(13.14)
float2_int = int(13.666)
print(type(float1_int), float1_int)
print(type(float2_int), float2_int)

